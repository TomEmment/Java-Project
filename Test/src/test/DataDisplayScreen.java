   private int[][]  Gatherdata() {  
    int x = data[0].length - 1;
    int test = 10;
    while(x>0){
        data[0][x] = 0;
        data[1][x] = 0;
        data[2][x] = 0;
        data[3][x] = 0;
        data[4][x] = 0;
        if (x<9)
        {
            data[0][x] = test;
            data[1][x] = test;
            data[2][x] = test +5;
            data[3][x] = test +7;  
            data[4][x] = (test+10)/2;
            test = test-1;
        }
        x=x-1;
    }
    return data; 
   }
   
